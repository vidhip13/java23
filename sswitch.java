//switch statement

public class sswitch {
        public static void main(String[] args){

            String city ="anand";
            switch(city)
            {
                case "Ahmedabad":
                System.out.println("city is ahmedabad");
                break;

                case "Surat":
                System.out.println("city is surat");
                break;

                case "Vadodara":
                System.out.println("city is vadodara");
                break;

                default:
                System.out.println("city is not anand");
                
            }
}
}